function eDeadEnd(arr) {
  let anyEliminated = false;
  let x = 1;
  while (x < arr.length - 1) {
    let y = 1;
    while (y < arr[0].length - 1) {
      if (arr[x][y] === 0) {
        let zeros = 0;
        if (arr[x - 1][y] === 0) {
          zeros += 1;
        }
        if (arr[x][y + 1] === 0) {
          zeros += 1;
        }
        if (arr[x + 1][y] === 0) {
          zeros += 1;
        }
        if (arr[x][y - 1] === 0) {
          zeros += 1;
        }
        if (zeros < 2) {
          arr[x][y] = 'X';
          anyEliminated = true;
        }
      }
      y += 1;
    }
    x += 1;
  }
  //console.log(arr);
  return [arr, anyEliminated];
}

function deadEnd(arr) {
  let x = 0;
  while (x < arr.length) {
    let y = 0;
    while (y < arr[0].length) {
      if (arr[x][y] === 0) {
        arr[x][y] = 'H';
      } else if (arr[x][y] === 'X') {
        arr[x][y] = 0;
      }
      y += 1;
    }
    x += 1;
  }
  //console.log(arr);
  return arr;
}

function maze(arr) {
  let keepEliminating = true;
  while(keepEliminating) {
    let eliminationResults = eDeadEnd(arr);
    arr = eliminationResults[0];
    keepEliminating = eliminationResults[1];
  }
  arr = deadEnd(arr);

  let result = "";
  for (let i = 0; i <= arr.length - 1; i++) {
    result += arr[i].join(',') + '\n';
  }

  console.log(result);
  return arr;
}

